# 5 - Faça um Programa que converta metros para centímetros.

print('=-=' * 15)
print('   CONVERSOR DE METROS PARA CENTÍMETROS')
print('=-=' * 15)

metros = float(input('Quantos metros deseja converter em centímetros: --> '))
resultado = metros * 100

print(f'{metros} metros é equivalente a {resultado:.2f} centímetros.')

